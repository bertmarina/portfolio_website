        <p><img class="big left" src="./story_images/andalusia_itinerary.JPG" alt=""> <br>
          I realized, shortly before deciding to embark on this travel, that I have not been back to Spain after my
          Erasmus in 2013/2014. Nevertheless, I felt like it happened just the day before.
          Among the different and interesting areas of Spain, Andalusia has always fascinated me a lot. The history, the
          traditions, the food, the cities… I had great expectations for all of this.
          <br> <br>
          Everything was planned a bit in a hurry and I soon realized (thanks to my colleagues) that the tickets for
          Alhambra go away veeeery fast. Thankfully, there was still some tickets for one day in the week I was
          travelling so I had to make the best out of it.
          <br> <br>
          In the end, I decided to focus on three of the cities that called my attention plus Malaga, mostly because of
          the airport. If you feel like having a full Spanish immersion, you can also play the playlist I associated to
          each day.
        </p>
        <p>
          <b>Day 1</b> <br>
          <b>Soundtrack: Sevilla tiene un color especial</b> <br>
          <img class="big right" src="./story_images/andalusia_sevilla.jpg" alt="">
          I arrived in Malaga around lunch time. I left the grey and snowy (!) Zurich to meet a warm and sunny Spain.
          From the airport, I took a bus and there another one straight to <b>Seville</b>. From the bus station I walked
          a bit
          and found my hostel. It was very nice and clean and, most importantly, very close to the city center.
          I walked there right away and one of the first thing that I noticed were all the women dressed up with the
          typical Sevillana dress. They all looked amazing!
          The reason was we were in the <b>Feria de Abril</b> week so actually one of the most crowded period of the
          year.
          Well, you do not always get to see so many nice clothes at once! This first day was just very explorative. I
          walked around the cathedral, which is huge and impressive, down to the Guadalquivir river and finally had some
          tapas to finish the day.
        </p>
        <p>
          <b>Day 2</b> <br>
          <b>Soundtrack: Habanera from Bizet’s Carmen</b> <br>
          <img class="small left" src="./story_images/andalusia_sevilla1.jpg" alt="">
          On the second day in Sevilla, I took a walking tour in the morning to explore the city and get to know some
          curiosities. The guide was a nice and young guy born and raised in the city, therefore with a typical
          southern, funny, accent. We got to know the story of la <b>Cabeza del Rey Don Pedro</b> which is quite funny.
          This king was often going out during the night and once he had a fight and killed a man. Unfortunately, that
          man was a noble and the day after the rumor about the killing was spread. The king, not knowing he was the man
          he killed himself, promised the head of the murderer. Also, that night, an old woman witnessed the fact and
          confessed it to the king. At that point, the kind decided to put a statue of his head in the street where the
          killing happened and, in that way, he kept his word.
          We then walked towards the <b>Cathedral</b>, one of the biggest in the world. The bell tower, <b>La
            Giralda</b>, with the Giraldillo on the top, is the symbol of the city. The interesting fact is that there
          are no stairs to go up but ramps as it was previously a minaret and so it was possible for the muezzin to go
          up and down with his horse.
          <img class="small right" src="./story_images/andalusia_sevilla2.jpg" alt="">
          The inside of the cathedral is as spectacular as the inside. It is full of chapels and decorations but one of
          the main points of attraction is the tomb of <b>Cristoforo Colombo</b>.
          To go back to the walking tour, we finished it just in front of the old <b>Royal Tobacco Factory</b>, now part
          of the university but forever remembered as the place where Carmen, the main character of Bizet’s masterpiece,
          used to work. <br>
          In the afternoon, while the temperature was turning higher and higher, I decided to walk to <b>Plaza de
            España</b>, a nice area with fountains and tiles representing all the provinces of Spain. But, most
          importantly, this location was used for Episode II of Star Wars!
          Before entering the <b>Real Alcazar</b> at 6 (good that I booked in advance!), I sat in a bar just in front of
          the cathedral enjoying a nice cocktail that is apparently an Andalusian invention: rebujito. It is basically a
          mix of sherry, manzanilla or fino, and soft drink. It is worth a try! At 6, finally, I entered the Real
          Alcazar and it was like entering a different time and world. The spaces are just so harmonic and decorated to
          give pleasure but not tire. The multiple rooms and corners hide fountains and a magnificent garden that makes
          the air fresh and reinvigorating. I was just wandering around amazed at everything my eyes were recording. Not
          even the people were disturbing me. It was just beautiful. <br>
          The day, however, was not finished, yet, as I booked a show at <b>La Casa de la Memoria</b>, a nice place
          where to see quite authentic flamenco. I am not a huge fan of this dance but how can you leave Sevilla without
          having had a taste of one of its most important traditions? The show was indeed nice and the performers very
          good!
        </p>
        <p>
          <b>Day 3</b> <br>
          <b>Soundtrack: Sevilla by Miguel Bosé (2007 version)</b> <br>
          The time in Sevilla was almost gone as I booked the bus to Granada in the late morning of day 3. This gave me
          the chance to have a quick walk to <b>Triana</b>, the gypsy area of the city. However, in the morning it was
          pretty empty and still sleepy. <br>
          Then, I moved on to the next city arriving in Granada just in time for lunch. I checked in in the hotel (for
          once a room only for me) and then walked into the cathedral area. I had a good lunch in the main square
          enjoying a guy that was singing and playing the violin. He seemed to be in the wrong place, though, when he
          started to sing Irish songs, but his voice was beautiful.
          <img class="big left" src="./story_images/andalusia_granada.jpg" alt="">
          The <b>Cathedral</b> was beautiful even if not as the one in Sevilla. Just next to it, actually in the same
          building but with different access, there is the more interesting <b>Capilla Mayor</b>, where Isabel and
          Fernando are buried along with their daughter Juana and her husband and son. The space is not huge and the
          tombs in the crypt simple but the marble decoration that reproduces their sleeping bodies is incredible. They
          really seem real and sleeping! Also, the more I was traveling, the more I was getting to know these historical
          characters that created Spain as we know it today. <br>
          In Granada, I was supposed to meet my friend Timon as he lived there but, as he was busy with his cinema
          classes, I thought the best idea was to do a walking tour and there was the one to the <b>Sacromonte</b>
          available, as the next day I had the more general in the city one booked already. A suggestion? If you go to
          Granada, just go at sunset on top of Sacromonte and you will immediately fall in love with the city. It is
          just impossible not to. I was a bit hesitant at the beginning to go alone as this is still the gypsy area,
          where people live in caves, and it is not 100% safe wandering there alone. True, but not completely. We walked
          to the top with a great guy explaining the stories and traditions of Alhambra, Granada, the gypsy community…
          and then, <b>Alhambra</b> was there. I am not exaggerating when saying I was almost crying. Somehow, that
          place hit me hard and moved me. It is like I was there in some previous life and recognized this view. After
          reaching the top, we started slowly to get down on the other side. Today, many of the caves have been
          transformed into restaurants and bars but also modern houses even if there are still some “authentic”. Well, I
          am glad I don’t live in one of those!
          As the tour ended, I finally met Timon to have dinner together. It was funny to have a reunion here but mostly
          great to get to see him after some time.
        </p>
        <p>
          <b>Day 4</b> <br>
          <b>Soundtrack: Como el Agua of Camarón de la Isla (not from Granada but still good!)</b> <br>
          On this full-day in Granada, as I said, I started with a general walking tour of the city. Timon came as well
          and he also discovered new things. We walked to see the cathedral, the <b>Lonja</b> and some of the main
          squares. We then also went to the <b>Albaycín</b>, the area of the city on the hill with white houses. It is
          the oldest area and super well preserved. However, it did not become just a tourist attraction as there are
          many locals still living and spending their time there. Of course, it is also full of viewing points of
          Alhambra and we got our very classical photo there! <br>
          <img class="big right" src="./story_images/andalusia_granada1.jpg" alt="">
          In the afternoon, since it was not so hot (joke, we were dying!), we decided to climb on the Alhambra hill as
          Timon wanted to show me a cemetery. Yes, a cemetery. It was indeed very interesting (so many things you can
          learn from visiting the cemeteries… but that is another story) and it also had a wonderful view of the
          <b>Sierra Nevada</b>. We then just kept walking and descended on the other side, so we also saw some graffiti
          of <b>El Niño</b>. They are very beautiful and are so integrated into the city you could not imagine it
          without them! <br>
          That was a lot of walking and a lot of up and down under the sun, so we went to get what is said to be the
          best <b>churros con chocolate</b> in the city. I am not sure if this is true, but they were really good! Maybe
          the hot chocolate was not ideal with the temperature, but it felt right. Time to relax and shower a bit and
          then we were out again for dinner. <br>
          **DISCLAIMER** <br>
          Spain is famous for tapas, but I had the impression getting really good ones for a fair price was mission
          impossible. Everywhere there are just so many tourists that the locals move out to other areas and the food
          you find around is not the best. <br>
          **DISCLAIMER** <br>
          That said, we indeed went to eat in a nice place. We went there also the day before but, somehow, we managed
          to order the wrong things. The second time was better, and we had a lot of good food: croquetas, tuna, salmon,
          vegetables…we were fed and happy! Of course, to digest all that food we needed a walk and we went to the
          Albaycín again. The atmosphere was different again: few lights, Alhambra more than beautiful, and people here
          and there playing the guitar.
        </p>
        <p>
          <b>Day 5</b> <br>
          <b>Soundtrack: Recuerdos de la Alhambra</b> <br>
          The last day in Granada arrived in the end. I will never be grateful enough to the people who advised me on
          getting the tickets for Alhambra in advance. I managed to find the only spot available in the entire week! So,
          <b>Alhambra</b>. After writing you about the magic surrounding this place, I can tell you more about the
          inside of it. It was like being in a dream. I was not open mouth all the time just because I was repeating
          myself there were other people around, but it was simply so beautiful!
          <img class="big left" src="./story_images/andalusia_granada2.jpg" alt="">
          I arrived in the morning at the opening which was perfect because there were few people, at least for the
          first half an hour, and it was still quite fresh. I entered from a side entry and went directly to the
          <b>Alcazaba</b>, the point at the very end of the hill from which you see all the city. There are some
          towers but not much left in terms of buildings as this is the most ancient part and the proper fortress.
          Afterward, I went to the <b>Palacios Nazaries</b>, the proper royal palace made of infinite rooms and
          decorations. Here, the Arabs first and then Ferdinando and Isabella ruled and lived. I totally understand
          why, fighting with all their strengths against the unfaithful enemies, the Christian kings were anyway
          finding Granada a beautiful place where to stay. Every room is a surprise. There are arches, arabesques,
          colors, fountains, plants, smells around every corner and what we see now is not even one-third of how
          marvelous it was when built as the palace lived a long period of decadence until modern days when it was
          restored and returned to us. Part of the Alhambra complex is also the <b>Generalife</b>, a summer palace
          surrounded by gardens and fountains. Walking through these places you do not even perceive the hot weather
          as it is fresh and shaded all around and the vegetation is lush, not dry and yellowish as you would expect.
          Something is certain, the sultans knew how to treat themselves! <br>
          After this amazing morning, I took the bus to Córdoba, three hours ride in the hilly and mostly empty
          Andalusia.
          <img class="big right" src="./story_images/andalusia_cordoba.jpg" alt="">
          The first impact is this new city was: why is there no bus going from the station to my hostel? So, I walked
          a good half an hour there but I was then repaid as the hostel I chose was indeed very nice! It was in the
          <b>San Basilio</b> area that I then discovered was one of the richest in terms of patios. I showered
          immediately because also in Córdoba was very hot. Then, I went out for a walk and after five minutes I saw
          some people queuing up at the entrance of the <b>Real Alcazar</b>. Without knowing really why, I stayed
          there and after few minutes, they opened the entrance for free for everybody! I took the chance to then do
          some visiting already and the palace was indeed interesting. The palace itself does not preserve much of how
          it was but it was worth going up on the towers to have an overview of the entire city. The gardens outside,
          though, were the real gem of the complex. Another interesting piece of historical information was that
          Cristoforo Colombo met in this palace for the first time Ferdinando and Isabella and in the middle of the
          gardens, there is a statue in memory of that. <br>
          After the palace, I kept on walking and I reached the famous <b>Mezquita</b>. From the outside, it seems
          just a big bricks cube with fake, decorated entrances. However, they witness the original use of the
          building, a mosque, which had many openings to always keep the inside fresh and windy. In Córdoba, the same
          river of Sevilla flows and there is an ancient Roman bridge that crosses it just in the city center. The
          view and the sunset from there were breathtaking. As you can imagine now, I was already taken by Córdoba as
          well! <br>
          Dinner here was also good. I had <b>salmorejo</b> (similar to gazpacho but with bread mixed with tomatoes)
          and I loved it so much that I had it also next day for lunch and dinner!
        </p>
        <p>
          <b>Day 6</b> <br>
          <b>Soundtrack: All souls night – Loreena McKennit</b> <br>
          New city, new walking tour! The group was small, so it was easier to understand and move to each place. This
          city has legends, too, and in particular one about a wedding and multiple killings. I don’t remember the
          details, but I do remember at one point there was someone’s head rolling down in a street… Apart from that,
          Córdoba is very interesting and its history fascinating. It is still possible to admire a Roman temple and
          then the different stratifications of Muslim and Christian conquests. <br>
          <img class="small left" src="./story_images/andalusia_cordoba1.jpg" alt="">
          Notable people are also from this city: <b>Seneca</b> for one, that I remember for his complicated
          philosophical works I had to translate in school, but also <b>Averroes</b>, a Muslim philosopher and thinker.
          The highlight of the day was, however, the <b>Mezquita</b>. It seems unreal to enter a place you have seen in
          Art History books and realize it exists for real. What is really incredible is the baroque church inside. It
          is such a strange mixing that you are not really sure the two places are indeed just one. <br>
          After a quick lunch (salmorejo), I took a bus to go to <b>Medina Azahara</b>. It is not far from Córdoba and
          it is definitely worth a visit. This city, built and abandoned between the X and XI centuries, was discovered
          in 1911 and since then the archaeologists kept excavating. The city had to be the symbol of the power of the
          first Andalusian calif. There was an urbanistic plan and a lot of money was spent on this project. However,
          after only 80 years from its construction, a civil war was determinant in its slow decline and final
          destruction. Today, you can still see the plan and in a close museum what was found during the excavations
          even if many construction materials were taken and reused somewhere else. <br>
          Once back in the city, I decided it would have been a good conclusion of the day to go see the <b>patios</b>.
          The week I was there was in fact the <b>Patio Festival 2019</b>. I had no idea, but patios are very typical in
          the houses of Córdoba as it was the common space of many families (water, toilet, kitchen) who lived in the
          same complex but also a way to keep the air cooler. Nowadays, patios compete to be elected the best of the
          city and win a good amount of money to spend on their conservation. They are taken care of in all the details:
          from the flowers and plants, green and lush with powerful smells, to the color and the decorations. The spaces
          are small and to enter inside there were some long queues. It must have been a particular day because all the
          neighborhood was partying and I got some street food for dinner (guess what? Salmorejo!). To conclude the day,
          I finally went into a restaurant/bar to get a <b>fiti fiti</b>, a mix of sweet wine Pedro Ximénez and white
          wine fino. Strong but good!
        </p>
        <p>
          <b>Day 7</b> <br>
          <b>Soundtrack: Imagina – Chambao</b> <br>
          Unfortunately, it was already time to leave Córdoba so I walked back to the bus station (still did not manage
          to find a bus!). The final destination was Málaga, more obliged because of the airport than because I wanted
          to visit it. Indeed, I did not like it. It was full of tourists, mostly young, partying people, not really my
          type, but also the city itself was ugly. Since there was not so much to do, I just went to see the
          <b>Alcazaba</b>, a Muslim fortress of which very few parts are left. From there, I went to the <b>Gibralfaro
            Castle</b>, still a Muslim fortress but on a higher hill. It was hot. Really hot. I don’t know how I managed
          not to faint as all the way up there was under the sun and it was 2 PM (I admit it, it was not the most
          brilliant idea I ever had). However, if the walk was hard, it was not repaid. The view of Málaga is pretty
          ugly as nothing has remained, and everything is new. Moreover, after being in three different and more
          beautiful alcazares, these fortresses were just meh. <br>
          <img class="big right" src="./story_images/andalusia_malaga.jpg" alt="">
          The evening I was thinking of having just a quick dinner somewhere in the city center as the flight was the
          morning after so no chance to enjoy the movida even if I wanted to. At the last minute, however, a girl I met
          during my Erasmus texted me as she lives in Malaga and saw my pictures on Instagram and we ended up having
          dinner together is an amazing place! If you did not know about it, you simply would have never seen it. It was
          very traditional with pictures of Jesus, Mary, all possible saints and processions everywhere and the food was
          super tasty! In the end, the day in Malaga was not so bad!
        </p>
        <p>And so it comes the end of an amazing travel in a more amazing country and the only thing I can say is ¡Hasta
          Pronto!</p>
